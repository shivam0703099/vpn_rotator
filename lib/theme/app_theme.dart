  /// Status colors for VPN connection states
  static Color getStatusColor(String status, {bool isDark = false}) {
    switch (status.toLowerCase()) {
      case 'connected':
        return isDark ? successDark : successLight;
      case 'connecting':
      case 'reconnecting':
        return isDark ? warningDark : warningLight;
      case 'disconnected':
      case 'error':
        return isDark ? errorDark : errorLight;
      default:
        return isDark ? onSurfaceVariantDark : onSurfaceVariantLight;
    }
  }

  /// Get monospace font for technical data (IP addresses, connection times)
  static TextStyle getMonospaceStyle({
    required bool isLight,
    double fontSize = 14,
    FontWeight fontWeight = FontWeight.w400,
  }) {
    return GoogleFonts.robotoMono(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: isLight ? onSurfaceLight : onSurfaceDark,
    );
  }
